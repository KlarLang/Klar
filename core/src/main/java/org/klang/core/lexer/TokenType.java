package org.klang.core.lexer;

public enum TokenType {
    USE,

    // Single-Character
    LPAREN, RPAREN, LBRACE, RBRACE, LBRACKET, RBRACKET,
    COMMA, SEMICOLON, COLON, DOT,
    PLUS, MINUS, MULTIPLY, DIVISION, REMAINDER, ASSIGNMENT,
    LT, GT, BANG,

    // Multi-Character
    INCREMENT, DECREMENT,
    LTE, GTE, DOUBLEEQUAL, NOTEQUAL, AND, OR, ARROW,

    // Generics
    NUMBER, IDENTIFIER,

    // Keywords
    RETURN, IF, OTHERWISE, AFTERALL, BECAUSE,
    FOR, WHILE, BREAK, CONTINUE, TRY, CATCH,
    PUBLIC, INTERNAL, PROTECTED, STATIC,
    TRUE, FALSE, NULL, CHARACTER_LITERAL, STRING_LITERAL, VOID, NEW,

    // Types
    INTEGER, DOUBLE, BOOLEAN, CHARACTER_TYPE, STRING_TYPE,

    // Especial
    AT, EOF
}
